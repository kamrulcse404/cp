#include<iostream>
using namespace std;

int main()
{
    int i;
    cin >> i;
    cout << "the value of the integer i = " << i << endl;

    long long l;
    cin >> l;
    cout << "the value of the long long l = " << l << endl;

    char c;
    cin >> c;
    cout << "the value of character c = "<< c << endl;

    float f;
    cin >> f;
    cout << "the value of the float f = " << f << endl;

    double d;
    cin >> d;
    cout << "the value of the double d = " << d << endl;

    return 0;
}
